def _format_outline_angle_value(value: float | None) -> str:
    formatted = f"{float(value or 0.0): .14E}"
    mantissa, exponent = formatted.split("E", 1)
    sign = exponent[0]
    digits = exponent[1:].rjust(4, "0")
    return f"{mantissa}E{sign}{digits}"


