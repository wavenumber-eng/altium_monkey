    def add_embedded_model(
        self,
        *,
        name: str,
        model_data: bytes,
        model_id: uuid.UUID | str | None = None,
        rotation_x_degrees: float = 0.0,
        rotation_y_degrees: float = 0.0,
        rotation_z_degrees: float = 0.0,
        z_offset_mil: float = 0.0,
        checksum: int | None = None,
        model_source: str = "Undefined",
        data_is_compressed: bool = False,
    ) -> AltiumPcbModel:
        """
        Add an embedded 3D model payload to the board.

        When `checksum` is omitted, the checksum is computed with Altium's
        native byte-weighted model checksum algorithm. Pass `checksum` only
        when preserving source metadata exactly during a copy workflow.

        Args:
            name: Model filename stored in `Models/Data`.
            model_data: Model payload bytes. Pass uncompressed bytes by default,
                or zlib-compressed payload stream bytes when
                `data_is_compressed=True`.
            model_id: Optional model GUID. A new GUID is generated when omitted;
                pass a deterministic GUID only for repeatable generated output.
            rotation_x_degrees: Default model X-axis rotation in degrees.
            rotation_y_degrees: Default model Y-axis rotation in degrees.
            rotation_z_degrees: Default model Z-axis rotation in degrees.
            z_offset_mil: Default model Z offset in mils.
            checksum: Optional native checksum override for metadata-preserving
                copy workflows.
            model_source: Altium model source string.
            data_is_compressed: True when `model_data` is already a compressed
                `Models/<n>` payload.

        Returns:
            The authored embedded model metadata object.
        """
        model = add_embedded_model_spec(
            self._embedded_models,
            name=name,
            model_data=model_data,
            model_id=model_id,
            rotation_x_degrees=rotation_x_degrees,
            rotation_y_degrees=rotation_y_degrees,
            rotation_z_degrees=rotation_z_degrees,
            z_offset_mil=z_offset_mil,
            checksum=checksum,
            model_source=model_source,
            data_is_compressed=data_is_compressed,
        )
        existing_by_id = {
            str(existing.id).upper(): existing for existing in self.models
        }
        existing_by_id[str(model.id).upper()] = model
        self.models = list(existing_by_id.values())
        self._models_dirty = True
        return model

    def add_component_body(
        self,
        body: AltiumPcbComponentBody,
        *,
        component_index: int | None = None,
    ) -> "PcbDocBuilder":
        body.component_index = self._normalize_component_index(component_index)
        self.component_bodies.append(body)
        self.shapebased_component_bodies.append(clone_body_for_shapebased_stream(body))
        self._component_bodies_dirty = True
        guid = make_authored_component_body_guid(
            body,
            sum(
                1
                for type_id, _guid in self._authored_primitive_guids
                if type_id == PcbGuidType.COMPONENT_BODY
            ),
        )
        self._register_authored_primitive_guid(PcbGuidType.COMPONENT_BODY, guid)
        self._register_authored_primitive_guid(
            PcbGuidType.SHAPEBASED_COMPONENT_BODY, guid
        )
        return self

    def _register_authored_primitive_guid(
        self, type_id: PcbGuidType | int, guid: uuid.UUID
    ) -> None:
        self._authored_primitive_guids.append((PcbGuidType(type_id), guid))

    def _build_uniqueid_primitive_information_stream(self) -> tuple[bytes, bytes]:
        if not self._pads_dirty:
            return (
                self._streams.get("UniqueIDPrimitiveInformation/Header", _ZERO_HEADER),
                self._streams.get("UniqueIDPrimitiveInformation/Data", b""),
            )

        existing_header = self._streams.get(
            "UniqueIDPrimitiveInformation/Header", _ZERO_HEADER
        )
        existing_data = self._streams.get("UniqueIDPrimitiveInformation/Data", b"")
        existing_count = (
            struct.unpack("<I", existing_header)[0] if len(existing_header) == 4 else 0
        )
        existing_pad_count = len(parse_pad_stream(self._streams.get("Pads6/Data", b"")))

        records = bytearray(existing_data)
        for offset, unique_id in enumerate(self._authored_pad_unique_ids):
            records.extend(
                build_pad_unique_id_info_record(
                    primitive_index=existing_pad_count + offset,
                    unique_id=unique_id,
                )
            )
        return struct.pack(
            "<I", existing_count + len(self._authored_pad_unique_ids)
        ), bytes(records)

    def _build_primitive_guids_data(self) -> PcbDocPrimitiveGuidsData:
        records = list(self.primitive_guids_data.records)
        if not self._authored_primitive_guids:
            return self.primitive_guids_data
        next_index_by_type: dict[int, int] = {}
        for record in records:
            next_index_by_type[record.type_id] = max(
                next_index_by_type.get(record.type_id, -1),
                int(record.index),
            )
        for type_id, guid in self._authored_primitive_guids:
            next_index = next_index_by_type.get(type_id, -1) + 1
            next_index_by_type[type_id] = next_index
            records.append(
                PcbDocPrimitiveGuidRecord(
                    type_id=type_id,
                    index=next_index,
                    guid=guid,
                )
            )
        return PcbDocPrimitiveGuidsData(records=tuple(records))

    def build_streams(self) -> dict[str, bytes]:
        streams = dict(self._streams)

        for stream_name in _EMPTY_DATA_STREAMS:
            streams[stream_name] = b""

        for stream_name in (
            "Arcs6/Header",
            "ComponentBodies6/Header",
            "Components6/Header",
            "Connections6/Header",
            "Coordinates6/Header",
            "DifferentialPairs6/Header",
            "Dimensions6/Header",
            "EmbeddedBoards6/Header",
            "EmbeddedFonts6/Header",
            "Embeddeds6/Header",
            "ExtendedPrimitiveInformation/Header",
            "Fills6/Header",
            "FromTos6/Header",
            "Models/Header",
            "ModelsNoEmbed/Header",
            "Nets6/Header",
            "PadViaLibrary/Header",
            "PadViaLibraryCache/Header",
            "PadViaLibraryLinks/Header",
            "Pads6/Header",
            "PinPairsSection/Header",
            "Polygons6/Header",
            "PrimitiveParameters/Header",
            "Regions6/Header",
            "ShapeBasedComponentBodies6/Header",
            "ShapeBasedRegions6/Header",
            "SmartUnions/Header",
            "Textures/Header",
            "Tracks6/Header",
            "UniqueIDPrimitiveInformation/Header",
            "Vias6/Header",
            "WaivedViolations/Header",
            "WideStrings6/Header",
        ):
            streams[stream_name] = _ZERO_HEADER

        streams["LayerKindMapping/Header"] = b"\x01\x00\x00\x00"
        streams["LayerKindMapping/Data"] = _DEFAULT_LAYER_KIND_MAPPING_DATA
        streams["FileHeader"] = self.file_header_data.build_stream()
        streams["FileHeaderSix"] = _build_file_header_six(
            self.profile.file_header_six_id,
            self.profile.file_header_six_guid,
        )
        streams["PadViaLibrary/Data"] = self.pad_via_library_data.build_stream()
        streams["PadViaLibraryCache/Data"] = (
            self.pad_via_library_cache_data.build_stream()
        )
        streams["ConstraintManager/Header"] = b"\x01\x00\x00\x00"
        streams["ConstraintManager/Data"] = self.constraint_manager_data.build_stream()
        streams["SignalClasses/Header"] = b"\x01\x00\x00\x00"
        streams["SignalClasses/Data"] = self.signal_classes_data.build_stream()
        streams["UnionNames/Header"] = b"\x01\x00\x00\x00"
        streams["UnionNames/Data"] = b"\x00\x00\x00\x00"
        streams["Board6/Header"] = self.board_data.build_header()
        streams["Board6/Data"] = self.board_data.build_stream()
        streams["Rules6/Header"] = self.rules_data.build_header()
        streams["Rules6/Data"] = self.rules_data.build_stream()
        streams["FileVersionInfo/Header"] = self.file_version_info_data.build_header()
        streams["FileVersionInfo/Data"] = self.file_version_info_data.build_stream()
        streams["Classes6/Header"] = self.classes_data.build_header()
        streams["Classes6/Data"] = self.classes_data.build_stream()
        streams["BoardRegions/Header"] = self.board_regions_data.build_header()
        streams["BoardRegions/Data"] = self.board_regions_data.build_stream()
        primitive_guids_data = self._build_primitive_guids_data()
        streams["PrimitiveGuids/Header"] = primitive_guids_data.build_header()
        streams["PrimitiveGuids/Data"] = primitive_guids_data.build_stream()
        uniqueid_header, uniqueid_data = (
            self._build_uniqueid_primitive_information_stream()
        )
        streams["UniqueIDPrimitiveInformation/Header"] = uniqueid_header
        streams["UniqueIDPrimitiveInformation/Data"] = uniqueid_data
        streams["Texts/Header"] = self.texts_data.build_header()
        streams["Texts/Data"] = self.texts_data.build_stream()
        streams["Design Rule Checker Options6/Header"] = b"\x01\x00\x00\x00"
        streams["Design Rule Checker Options6/Data"] = (
            self.design_rule_checker_options_data.build_stream()
        )
        streams["Pin Swap Options6/Header"] = b"\x01\x00\x00\x00"
        streams["Pin Swap Options6/Data"] = self.pin_swap_options_data.build_stream()
        streams["Advanced Placer Options6/Header"] = b"\x01\x00\x00\x00"
        streams["Advanced Placer Options6/Data"] = (
            self.advanced_placer_options_data.build_stream()
        )
        streams["SimbeorCacheSection/Header"] = (
            self.simbeor_cache_header_data.build_stream()
        )
        streams["SimbeorCacheSection/Data"] = self.simbeor_cache_data.build_stream()
        if self._nets_dirty:
            streams["Nets6/Header"] = struct.pack("<I", len(self.nets))
            streams["Nets6/Data"] = build_net_stream(self.nets)
        else:
            streams["Nets6/Header"] = self._streams.get(
                "Nets6/Header", struct.pack("<I", len(self.nets))
            )
            streams["Nets6/Data"] = self._streams.get("Nets6/Data", b"")
        if self._components_dirty:
            streams["Components6/Header"] = struct.pack("<I", len(self.components))
            streams["Components6/Data"] = build_component_stream(self.components)
        else:
            streams["Components6/Header"] = self._streams.get(
                "Components6/Header", struct.pack("<I", len(self.components))
            )
            streams["Components6/Data"] = self._streams.get("Components6/Data", b"")
        if self._component_parameters_dirty:
            params_header, params_data = build_component_parameter_stream(
                self.components
            )
            streams["PrimitiveParameters/Header"] = params_header
            streams["PrimitiveParameters/Data"] = params_data
        else:
            streams["PrimitiveParameters/Header"] = self._streams.get(
                "PrimitiveParameters/Header", _ZERO_HEADER
            )
            streams["PrimitiveParameters/Data"] = self._streams.get(
                "PrimitiveParameters/Data", b""
            )
        if self._fills_dirty:
            streams["Fills6/Header"] = struct.pack("<I", len(self.fills))
            streams["Fills6/Data"] = build_fill_stream(self.fills)
        else:
            streams["Fills6/Header"] = self._streams.get(
                "Fills6/Header", struct.pack("<I", len(self.fills))
            )
            streams["Fills6/Data"] = self._streams.get("Fills6/Data", b"")
        if self._models_dirty:
            embedded_models = [spec.model for spec in self._embedded_models]
            write_models_no_embed = (
                self._models_stream_source == "ModelsNoEmbed/Data"
                or bool(self.profile.raw_streams.get("ModelsNoEmbed/Data"))
                or len(embedded_models) != len(self.models)
            )
            if write_models_no_embed:
                streams["ModelsNoEmbed/Header"] = struct.pack("<I", len(self.models))
                streams["ModelsNoEmbed/Data"] = build_model_stream(self.models)
                streams["Models/Header"] = struct.pack("<I", len(embedded_models))
                streams["Models/Data"] = build_model_stream(embedded_models)
            else:
                streams["ModelsNoEmbed/Header"] = _ZERO_HEADER
                streams["ModelsNoEmbed/Data"] = b""
                streams["Models/Header"] = struct.pack("<I", len(self.models))
                streams["Models/Data"] = build_model_stream(self.models)
            for key in list(streams):
                if (
                    key.startswith("Models/")
                    and key.count("/") == 1
                    and key.split("/", 1)[1].isdigit()
                ):
                    streams.pop(key, None)
            for index, spec in enumerate(self._embedded_models):
                streams[f"Models/{index}"] = spec.embedded_payload
        else:
            streams["Models/Header"] = self._streams.get("Models/Header", _ZERO_HEADER)
            streams["Models/Data"] = self._streams.get("Models/Data", b"")
            streams["ModelsNoEmbed/Header"] = self._streams.get(
                "ModelsNoEmbed/Header", _ZERO_HEADER
            )
            streams["ModelsNoEmbed/Data"] = self._streams.get("ModelsNoEmbed/Data", b"")
            for key, value in self._streams.items():
                if (
                    key.startswith("Models/")
                    and key.count("/") == 1
                    and key.split("/", 1)[1].isdigit()
                ):
                    streams[key] = value
        if self._texts_dirty:
            table = self.widestrings_data.to_mapping()
            allocate_missing_widestring_indices(self.texts, table)
            for text in self.texts:
                if text.widestring_index is None:
                    continue
                table[int(text.widestring_index)] = text.text_content or ""
            self.widestrings_data = PcbDocWideStringsData.from_mapping(table)
            self.texts6_data = PcbDocTexts6Data.from_texts(self.texts)
            streams["WideStrings6/Header"] = self.widestrings_data.build_header()
            streams["WideStrings6/Data"] = self.widestrings_data.build_stream()
            streams["Texts6/Header"] = self.texts6_data.build_header()
            streams["Texts6/Data"] = self.texts6_data.build_stream()
        else:
            streams["WideStrings6/Header"] = self._streams.get(
                "WideStrings6/Header", self.widestrings_data.build_header()
            )
            streams["WideStrings6/Data"] = self._streams.get("WideStrings6/Data", b"")
            streams["Texts6/Header"] = self._streams.get(
                "Texts6/Header", self.texts6_data.build_header()
            )
            streams["Texts6/Data"] = self._streams.get(
                "Texts6/Data", self.texts6_data.build_stream()
            )
        if self._pads_dirty:
            streams["Pads6/Header"] = struct.pack("<I", len(self.pads))
            streams["Pads6/Data"] = build_pad_stream(self.pads)
        else:
            streams["Pads6/Header"] = self._streams.get(
                "Pads6/Header", struct.pack("<I", len(self.pads))
            )
            streams["Pads6/Data"] = self._streams.get("Pads6/Data", b"")
        if self._vias_dirty:
            streams["Vias6/Header"] = struct.pack("<I", len(self.vias))
            streams["Vias6/Data"] = build_via_stream(self.vias)
        else:
            streams["Vias6/Header"] = self._streams.get(
                "Vias6/Header", struct.pack("<I", len(self.vias))
            )
            streams["Vias6/Data"] = self._streams.get("Vias6/Data", b"")
        if self._regions_dirty:
            streams["Regions6/Header"] = struct.pack("<I", len(self.regions))
            streams["Regions6/Data"] = build_region_stream(self.regions)
            streams["ShapeBasedRegions6/Header"] = struct.pack(
                "<I", len(self.shapebased_regions)
            )
            streams["ShapeBasedRegions6/Data"] = build_shapebased_region_stream(
                self.shapebased_regions
            )
        else:
            streams["Regions6/Header"] = self._streams.get(
                "Regions6/Header", struct.pack("<I", len(self.regions))
            )
            streams["Regions6/Data"] = self._streams.get("Regions6/Data", b"")
            streams["ShapeBasedRegions6/Header"] = self._streams.get(
                "ShapeBasedRegions6/Header",
                struct.pack("<I", len(self.shapebased_regions)),
            )
            streams["ShapeBasedRegions6/Data"] = self._streams.get(
                "ShapeBasedRegions6/Data", b""
            )
        if self._component_bodies_dirty:
            streams["ComponentBodies6/Header"] = struct.pack(
                "<I", len(self.component_bodies)
            )
            streams["ComponentBodies6/Data"] = build_component_body_stream(
                self.component_bodies
            )
            streams["ShapeBasedComponentBodies6/Header"] = struct.pack(
                "<I", len(self.shapebased_component_bodies)
            )
            streams["ShapeBasedComponentBodies6/Data"] = build_component_body_stream(
                self.shapebased_component_bodies
            )
        else:
            streams["ComponentBodies6/Header"] = self._streams.get(
                "ComponentBodies6/Header", struct.pack("<I", len(self.component_bodies))
            )
            streams["ComponentBodies6/Data"] = self._streams.get(
                "ComponentBodies6/Data", b""
            )
            streams["ShapeBasedComponentBodies6/Header"] = self._streams.get(
                "ShapeBasedComponentBodies6/Header",
                struct.pack("<I", len(self.shapebased_component_bodies)),
            )
            streams["ShapeBasedComponentBodies6/Data"] = self._streams.get(
                "ShapeBasedComponentBodies6/Data", b""
            )
        streams["Arcs6/Header"] = struct.pack("<I", len(self.arcs))
        streams["Arcs6/Data"] = build_arc_stream(self.arcs)
        streams["Tracks6/Header"] = struct.pack("<I", len(self.tracks))
        streams["Tracks6/Data"] = build_track_stream(self.tracks)
        return streams

    def save(self, filepath: Path) -> Path:
        """
        Build the PCB document streams and write them to disk.

        This is the canonical public write path for builder output.
        """
        filepath = Path(filepath)
        filepath.parent.mkdir(parents=True, exist_ok=True)

        writer = AltiumOleWriter(clsid=self.profile.root_clsid)
        for stream_name, data in self.build_streams().items():
            writer.add_stream(stream_name, data)
        writer.write(filepath)
        return filepath
