def _build_length_prefixed_utf16le(body: str) -> bytes:
    body_bytes = body.encode("utf-16le")
    return struct.pack("<I", len(body_bytes)) + body_bytes


def _extract_length_prefixed_ascii_body(data: bytes) -> str:
    if len(data) < 4:
        raise ValueError("Invalid length-prefixed ASCII stream")
    length = struct.unpack("<I", data[:4])[0]
    if len(data) < 4 + length:
        raise ValueError("Invalid length-prefixed ASCII stream")
    return data[4 : 4 + length].decode("latin-1").rstrip("\x00")


def _extract_length_prefixed_utf16le_body(data: bytes) -> str:
    if len(data) < 4:
        raise ValueError("Invalid length-prefixed UTF-16LE stream")
    length = struct.unpack("<I", data[:4])[0]
    if len(data) < 4 + length:
        raise ValueError("Invalid length-prefixed UTF-16LE stream")
    return data[4 : 4 + length].decode("utf-16le").rstrip("\x00")


def _build_file_header_six(file_id: str, file_guid: uuid.UUID) -> bytes:
    file_id_bytes = file_id.encode("ascii")
    guid_text = f"{{{str(file_guid).upper()}}}"
    guid_bytes = guid_text.encode("ascii")
    buf = bytearray()
    buf.extend(struct.pack("<I", len(file_id_bytes)))
    buf.append(len(file_id_bytes))
    buf.extend(file_id_bytes)
    buf.extend(struct.pack("<d", 5.01))
    buf.extend(struct.pack("<I", len(guid_bytes)))
    buf.append(len(guid_bytes))
    buf.extend(guid_bytes)
    return bytes(buf)


def _parse_file_header_six(data: bytes) -> tuple[str, uuid.UUID]:
    if len(data) < 4:
        raise ValueError("Invalid FileHeaderSix stream")
    file_id_len = struct.unpack("<I", data[:4])[0]
    offset = 4
    if len(data) < offset + 1 + file_id_len + 8 + 4:
        raise ValueError("Invalid FileHeaderSix stream")
    pascal_len = data[offset]
    offset += 1
    file_id = data[offset : offset + pascal_len].decode("ascii")
    offset += file_id_len
    offset += 8  # version double
    guid_len = struct.unpack("<I", data[offset : offset + 4])[0]
    offset += 4
    if len(data) < offset + 1 + guid_len:
        raise ValueError("Invalid FileHeaderSix stream")
    guid_pascal_len = data[offset]
    offset += 1
    guid_text = data[offset : offset + guid_pascal_len].decode("ascii")
    return file_id, uuid.UUID(guid_text.strip("{}"))


