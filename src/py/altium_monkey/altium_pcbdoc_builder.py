@dataclass(frozen=True)
class PcbDocPrimitiveGuidsData:
    """
    Owner for the board-wide `PrimitiveGuids/*` table.
    """

    records: tuple[PcbDocPrimitiveGuidRecord, ...]

    @classmethod
    def from_bytes(cls, data: bytes) -> "PcbDocPrimitiveGuidsData":
        if len(data) % 24 != 0:
            raise ValueError(f"Unexpected PrimitiveGuids/Data size: {len(data)}")
        records: list[PcbDocPrimitiveGuidRecord] = []
        for offset in range(0, len(data), 24):
            type_id, index = struct.unpack("<II", data[offset : offset + 8])
            guid = uuid.UUID(bytes_le=data[offset + 8 : offset + 24])
            records.append(
                PcbDocPrimitiveGuidRecord(type_id=type_id, index=index, guid=guid)
            )
        return cls(records=tuple(records))

    @classmethod
    def default(cls) -> "PcbDocPrimitiveGuidsData":
        return cls.from_bytes(
            base64.b64decode(_DEFAULT_PCBDOC_PRIMITIVE_GUIDS_DATA_B64)
        )

    @property
    def record_count(self) -> int:
        return len(self.records)

    @property
    def type_ids(self) -> tuple[int, ...]:
        return tuple(record.type_id for record in self.records)

    def build_header(self) -> bytes:
        return struct.pack("<I", self.record_count)

    def build_stream(self) -> bytes:
        data = bytearray()
        for record in self.records:
            data.extend(struct.pack("<II", record.type_id, record.index))
            data.extend(record.guid.bytes_le)
        return bytes(data)


@dataclass(frozen=True)
class PcbDocLegacyTextRecord:
    """
    Single `Texts/Data` warning/message record.

    Current understanding is intentionally structural, not semantic-complete:
    - the first 256 bytes are a fixed binary prefix with font/style-like fields
    - the tail is a short ASCII warning string

    The prefix is preserved byte-for-byte until more of the legacy format is
    not fully known.
    """

    opaque_prefix: bytes
    message: str

    @classmethod
    def from_bytes(cls, data: bytes) -> "PcbDocLegacyTextRecord":
        if len(data) < 262:
            raise ValueError("Invalid legacy Texts/Data record")
        opaque_prefix = bytes(data[:256])
        zero_byte = data[256]
        char_count_plus_one = struct.unpack("<I", data[257:261])[0]
        char_count = data[261]
        if zero_byte != 0:
            raise ValueError("Unexpected legacy Texts/Data separator byte")
        if char_count_plus_one != char_count + 1:
            raise ValueError("Unexpected legacy Texts/Data message length")
        if len(data) != 262 + char_count:
            raise ValueError("Unexpected legacy Texts/Data record length")
        return cls(
            opaque_prefix=opaque_prefix,
            message=data[262:].decode("ascii"),
        )

    def build_stream(self) -> bytes:
        message_bytes = self.message.encode("ascii")
        if len(message_bytes) > 0xFF:
            raise ValueError("Legacy Texts/Data message too long")
        return (
            bytes(self.opaque_prefix)
            + b"\x00"
            + struct.pack("<I", len(message_bytes) + 1)
            + bytes((len(message_bytes),))
            + message_bytes
        )


@dataclass(frozen=True)
class PcbDocTextsData:
    """
    Owner for legacy `Texts/*` streams.

    This is not the same format as `Texts6/Data`. In the default builder profile
    it is a small table of warning/message records rather than PCB text
    primitives. Parsed boards also show that both stream families can coexist,
    so `Texts/*` should be treated as a separate legacy side-table rather than
    as an obsolete predecessor to `Texts6/*`.

    The 256-byte record prefix remains opaque data, but the record framing is
    understood well enough to own the stream structurally.
    """

    records: tuple[PcbDocLegacyTextRecord, ...]

    @classmethod
    def from_streams(cls, header_data: bytes, data: bytes) -> "PcbDocTextsData":
        if len(header_data) != 4:
            raise ValueError("Invalid Texts/Header stream")
        record_count = struct.unpack("<I", header_data)[0]
        records: list[PcbDocLegacyTextRecord] = []
        offset = 0
        for _ in range(record_count):
            if len(data) < offset + 262:
                raise ValueError("Invalid Texts/Data stream")
            char_count = data[offset + 261]
            record_size = 262 + char_count
            records.append(
                PcbDocLegacyTextRecord.from_bytes(data[offset : offset + record_size])
            )
            offset += record_size
        if offset != len(data):
            raise ValueError("Unexpected trailing bytes in Texts/Data stream")
        return cls(records=tuple(records))

    @property
    def record_count(self) -> int:
        return len(self.records)

    @property
    def messages(self) -> tuple[str, ...]:
        return tuple(record.message for record in self.records)

    def build_header(self) -> bytes:
        return struct.pack("<I", self.record_count)

    def build_stream(self) -> bytes:
        return b"".join(record.build_stream() for record in self.records)

    @classmethod
    def default(cls) -> "PcbDocTextsData":
        return cls(
            records=tuple(
                PcbDocLegacyTextRecord(
                    opaque_prefix=opaque_prefix,
                    message=message,
                )
                for opaque_prefix, message in zip(
                    _DEFAULT_TEXTS_WARNING_PREFIXES,
                    _DEFAULT_TEXTS_WARNING_MESSAGES,
                    strict=True,
                )
            )
        )


