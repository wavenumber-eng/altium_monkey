@dataclass(frozen=True)
class PcbDocRulesData:
    """
    Typed owner for the native `Rules6/Data` stream.

    The stream format is not a normal OLE text-record list: each rule is stored
    as `[2-byte leader][4-byte little-endian payload length][text payload]`.
    We reuse the existing typed `AltiumPcbRule` models so the builder and main
    parser stay aligned on rule semantics.
    """

    records: tuple[AltiumPcbRule, ...]

    @classmethod
    def from_bytes(cls, data: bytes) -> "PcbDocRulesData":
        rules: list[AltiumPcbRule] = []
        pos = 0
        while pos + 6 <= len(data):
            leader = data[pos : pos + 2]
            length = int.from_bytes(data[pos + 2 : pos + 6], byteorder="little")
            pos += 6
            if length <= 0 or pos + length > len(data):
                break
            raw_payload = data[pos : pos + length]
            pos += length
            record_bytes = (
                raw_payload[:-1] if raw_payload.endswith(b"\x00") else raw_payload
            )

            fields: dict[str, str] = {}
            for pair in parse_byte_record(record_bytes):
                text = decode_byte_array(pair)
                if "=" not in text:
                    continue
                key, value = text.split("=", 1)
                key_text = str(key or "").strip().upper()
                if key_text:
                    fields[key_text] = value

            if "RULEKIND" not in fields:
                continue

            rules.append(
                AltiumPcbRule.from_record(
                    fields,
                    index=len(rules),
                    record_leader=leader,
                    record_payload=raw_payload,
                )
            )

        return cls(records=tuple(rules))

    @classmethod
    def default(cls) -> "PcbDocRulesData":
        return cls.from_bytes(base64.b64decode(_DEFAULT_PCBDOC_RULES_DATA_B64))

    @property
    def rule_count(self) -> int:
        return len(self.records)

    @property
    def rule_names(self) -> tuple[str, ...]:
        return tuple(rule.name for rule in self.records)

    def build_header(self) -> bytes:
        return struct.pack("<I", self.rule_count)

    def build_stream(self) -> bytes:
        data = bytearray()
        for rule in self.records:
            data.extend(rule.record_leader or b"\x00\x00")
            if getattr(rule, "can_passthrough_raw_payload", lambda: False)():
                payload = bytes(getattr(rule, "raw_record_payload", b""))
                data.extend(len(payload).to_bytes(4, byteorder="little"))
                data.extend(payload)
                continue
            encoded = encode_altium_record(rule.to_record())
            data.extend(encoded[:4])
            data.extend(encoded[4:])
        return bytes(data)


