    def set_outline_circle_mils(
        self,
        *,
        center_mils: tuple[float, float],
        diameter_mils: float,
        vertex_count: int = 64,
    ) -> "PcbDocBuilder":
        """
        Approximate a circular board outline with evenly spaced vertices.

        Board6/Data stores outline geometry as a polygon, so authored circles
        are represented as a regular polygon approximation.
        """
        if diameter_mils <= 0:
            raise ValueError("Circle diameter must be positive")
        if vertex_count < 8:
            raise ValueError("Circle outline requires at least 8 vertices")

        center_x, center_y = center_mils
        radius_mils = diameter_mils / 2.0
        vertices: list[tuple[float, float]] = []
        for index in range(vertex_count):
            angle = 2.0 * math.pi * index / vertex_count
            x_mils = round(center_x + radius_mils * math.cos(angle), 6)
            y_mils = round(center_y + radius_mils * math.sin(angle), 6)
            vertices.append((x_mils, y_mils))
        return self.set_outline_vertices_mils(vertices)

