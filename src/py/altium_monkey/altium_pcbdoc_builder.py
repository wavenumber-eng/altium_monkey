    def _find_net_index(self, name: str) -> int | None:
        normalized = name.strip().upper()
        for index, net in enumerate(self.nets):
            if net.name.strip().upper() == normalized:
                return index
        return None

    def _normalize_component_index(self, component_index: int | None) -> int | None:
        if component_index is None:
            return None
        normalized = int(component_index)
        if normalized < 0 or normalized >= len(self.components):
            raise IndexError(
                f"Component index {component_index} out of range for {len(self.components)} components"
            )
        return normalized

    def add_net(
        self,
        name: str,
        *,
        preferred_width_mils: float = 10.0,
    ) -> "PcbDocBuilder":
        if not name.strip():
            raise ValueError("Net name must not be empty")
        if self._find_net_index(name) is None:
            self.nets.append(
                build_authored_net(
                    name.strip(), preferred_width_mils=preferred_width_mils
                )
            )
            self._nets_dirty = True
        return self

